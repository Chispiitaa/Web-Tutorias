        <section id="about">
            <h2>Sobre el PIT</h2>
            <p>
                La tutoría en la educación superior se define como un acompañamiento personalizado proporcionado por un profesor capacitado, cuyo objetivo es apoyar el desarrollo integral del estudiante, promoviendo su crecimiento, madurez, y la adopción de actitudes de responsabilidad y libertad (ANUIES, 2012).
            </p>
            <p>
                <strong>Principales objetivos y roles de la tutoría:</strong>
            </p>
            <ul>
                <li>
                    <strong>Acompañamiento Personalizado:</strong> Un tutor debe guiar al estudiante en su desarrollo personal y académico, ayudándole a construir conocimientos y habilidades de manera autónoma (Moncada y Gómez, 2013).
                </li>
                <li>
                    <strong>Desarrollo de Pensamiento Crítico:</strong> La tutoría implica un enfoque interactivo y dialógico donde un tutor o un compañero más avanzado apoya al estudiante en el dominio de conocimientos y habilidades de argumentación (Peñalosa, 2017).
                </li>
                <li>
                    <strong>Apoyo Integral:</strong> Los tutores deben identificar y abordar situaciones de carencia o conflicto en los estudiantes, buscando prevenir el fracaso y la deserción escolar, así como fomentar la participación y el aprendizaje colaborativo (Duran & Vidal, 2004).
                </li>
                <li>
                    <strong>Innovación y Uso de Tecnologías:</strong> Los tutores deben actualizar sus prácticas educativas, integrando nuevas tecnologías y metodologías, como el aprendizaje basado en proyectos y el aprendizaje cooperativo (Álvarez, 2010).
                </li>
            </ul>
            <p>
                <strong>Impacto en el contexto global:</strong>
            </p>
            <p>
                La tutoría facilita la adaptación de los estudiantes a su entorno educativo, promoviendo su desarrollo integral en las esferas intelectual, afectiva, personal y social. Ayuda a canalizar las relaciones del alumnado con los diferentes segmentos de atención al estudiante y a utilizar adecuadamente los recursos curriculares y extracurriculares disponibles (Rodríguez, 2012).
            </p>
            <p>
                <strong>Importancia Institucional:</strong>
            </p>
            <p>
                La tutoría es considerada un componente clave para mejorar la calidad educativa y reducir el rezago y abandono escolar. Se recomienda la implementación de Programas Institucionales de Tutorías (PIT) como estrategia para apoyar al estudiante desde su ingreso y a lo largo de su formación (ANUIES, 2008).
            </p>
        </section>

        <section id="types-of-tutoring" class="mt-5">
            <h2>Tipos de Tutorías</h2>
            <ul>
                <li><strong>Tutoría Individual:</strong> Es un acompañamiento académico, personal o ambos que se realiza a lo largo del periodo escolar a petición de un alumno, quien ha identificado al tutor que puede apoyarlo en los temas de su interés.</li>
                <li><strong>Tutoría Grupal:</strong> Es la actividad de acompañamiento durante todo el periodo escolar, a un grupo validado y autorizado en la estructura académica, en cualquier modalidad y contenido dentro de su carga académica. El docente apoya en el fortalecimiento del sentido de pertenencia institucional, difunde los derechos y obligaciones, conoce los programas y servicios de apoyo disponibles en su Unidad Académica y en el IPN; en su caso realiza la canalización correspondiente.</li>
                <li><strong>Tutoría de Recuperación Académica:</strong> Es la acción dirigida a grupos de alumnos con dictamen de la Comisión de Situación Escolar (COSIE), en riesgo de rezago, abandono o que han recursado una Unidad de Aprendizaje y no la acreditaron; así como de unidades de aprendizaje de programas en liquidación. Esta acción tiene la finalidad de regularizar la trayectoria escolar de los alumnos. La tutoría debe contar con la aprobación de la Academia, el visto bueno del titular de la Unidad Académica y estar autorizada por la Dirección de Coordinación correspondiente.</li>
                <li><strong>Tutoría de Regularización:</strong> Es la acción dirigida a un grupo de alumnos que requieren reafirmar conocimientos, que no hayan acreditado su unidad de aprendizaje, por temas específicos de difícil comprensión o para mejorar el rendimiento académico en una Unidad de Aprendizaje.</li>
                <li><strong>Tutoría entre Pares:</strong> Esta actividad es desempeñada por alumnos de los Niveles Medio Superior o Superior con buen dominio en determinadas unidades de aprendizaje, las cuales frecuentemente tienen alto índice de reprobación. La tutoría entre pares se realiza en un espacio y horario asignado, y la labor del alumno tutor es explicar los temas que no han sido comprendidos parcial o totalmente, apoyar con ejemplos y resolución de problemas o ejercicios, en un ambiente de respeto y confianza, compartiendo estrategias de enseñanza que los jóvenes tutores han innovado y son de utilidad para que sus pares tengan mejor comprensión de las temáticas abordadas. Para el óptimo desarrollo de esta actividad es necesario que los alumnos tutores cuenten con el acompañamiento y coordinación de un docente tutor.</li>
            </ul>
        </section>

        <section id="types-of-intervention" class="mt-5">
            <h2>Tipos de Intervención</h2>
            <ul>
                <li><strong>Inductiva:</strong> Acciones que facilitan la adaptación del alumno al nuevo entorno escolar y a su modalidad educativa.</li>
                <li><strong>Preventiva:</strong> Acciones dirigidas al alumno que evitan riesgo de rezago, reprobación o abandono.</li>
                <li><strong>Correctiva:</strong> Se dirige a los alumnos que necesitan orientación y apoyo para recuperar su trayectoria académica.</li>
            </ul>
        </section>

        
